package com.rakovets.course.java.core.practice.oop_classes_and_objects.task03_rectangle;

public class PointToRectangle {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public PointToRectangle(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
